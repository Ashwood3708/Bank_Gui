    private void btnLogOnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogOnActionPerformed
        // TODO add your handling code here:

        frank = bank.authenticateCustomer(Integer.parseInt(txtID.getText()), Integer.parseInt(txtPin.getText()));
        if (frank == null) {
            JOptionPane.showMessageDialog(null, "no matches");
        } else {
            btnSave.setEnabled(true);
            btnWithDraw.setEnabled(true);
            btnDeposit.setEnabled(true);
            btnTransfer.setEnabled(true);
            btnBalance.setEnabled(true);
            btnLogOff.setEnabled(true);
        }
        //JOptionPane.showMessageDialog(null, frank.toString());

        String[] temp = new String[frank.getNumOfAccs()];
        for (int i = 0; i < frank.getNumOfAccs(); i++) {
            temp[i] = frank.getAccs(i).toString();
        }
        list1.setListData(temp);
        ret = new TransferList(this, true, frank);

        if (frank.getNumOfAccs() < 2) {
            btnTransfer.setEnabled(false);
        }


    }//GEN-LAST:event_btnLogOnActionPerformed

