    private void btnTransferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransferActionPerformed
        // TODO add your handling code here:
        JOptionPane.showMessageDialog(null, "choose first account to transfer funds");
        Account one = new Account();
        ret.setVisible(true);
        one = frank.getAccs(ret.getList().getSelectedIndex());

        Transaction T1 = new Transaction();
        int amount = Integer.parseInt(JOptionPane.showInputDialog("Enter amount to transfer"));
        T1.setTransactionType(TransactionType.debit);
        T1.setAmount(amount);
       while(one.getBalance()< amount){
       JOptionPane.showMessageDialog(null, "Withdraw exceeds Account balance");
       amount = Integer.parseInt(JOptionPane.showInputDialog("Enter amount to transfer"));
       }

        if (T1.getAmount() <= 0.0) {
            while (T1.getAmount() <= 0.0) {

                amount = Integer.parseInt(JOptionPane.showInputDialog("Transfer must be more than 0"));
            }
        } else {
            one.addTrans(T1);
            T1.setTransactionType(TransactionType.credit);

            JOptionPane.showMessageDialog(null, "choose second account to transfer funds");
            ret.setVisible(true);
            Account two = frank.getAccs(ret.getList().getSelectedIndex());

            while (one == two) {
                JOptionPane.showMessageDialog(null, "must choose a different account");
                ret.setVisible(true);
                two = frank.getAccs(ret.getList().getSelectedIndex());
            }

            two.addTrans(T1);

        }

    }//GEN-LAST:event_btnTransferActionPerformed

